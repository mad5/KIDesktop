<h2>Informationen zu KIDesktop</h2>

<h3>Die Idee</h3>


<p>
In der Grundschule fängt der erste Computer-Unterricht an. 
Zunächst geht es darum, das Gerät grundsätzlich zu verstehen, ein- und ausschalten, Maus, Tastatur, Monitor. 
Dann werden erste kurze Worte geschrieben, gemalt und kleine Programme ausprobiert. 
Über den Lese-Unterricht kommt dann <a href='https://www.antolin.de/' target=_blank>Antolin</a> ins Spiel, was gerne von Zuhause aus genutzt werden soll. 
Durch die weite Verbreitung von Tablets und Smartphones ist die Verwendung von Maus und Tastatur in den Hintergrund gerückt. 
Auch wenn in Zukunft mehr auf einem Touchscreen gearbeitet wird, ist der sichere Umgang mit einer Tastatur und die Hand-Auge-Koordination beim Umgang mit einer Maus nicht mehr aus einem Arbeitsalltag wegzudenken.
</p>
<p>
Hier setzt <b>KIDesktop</b> an. 
Im Grundschulalter kann ein eigener Computer dem Kind die Möglichkeit geben sich spielerisch mit dem Medium auseinander zu setzen. 
Viele der Angebote im Internet sind bestens geeignet. Aber der Computer sollte nicht ohne ausreichenden Schutz mit dem Internet verbunden werden 
und der Zugriff sollte eingeschränkt sein, so dass nicht in jungen Jahre eine Internetsucht gefördert wird. 
KIDesktop ist so ausgelegt, dass es zunächst den Zugriff nach außen komplett deaktiviert und der Nutzer nur über gezielte Freigaben mit Internet-Inhalten kommunizieren kann. 
Das erfordert Handarbeit bei der Freigabe von Inhalten, auf diese Weise behälten die Eltern aber die volle Kontrolle. 
Diese kann mit fortschreitendem Alter des Kindes dann stückweise abgegeben werden, wenn der Umgang mit dem Medium verantwortungsvoll erlernt wurde.</p>
<p>

Ein weiterer Aspekt bei einem "eigenen Computer" sind die Kosten. Es kann und will nicht jeder seinem Kind einen teuren Computer bezahlen. 
Hier kommt uns die aktuelle Entwicklung günstiger Kleinstcomputer entgegen. 
Es gibt mittlerweile günstige Komponenten, die sinnvoll zusammengestellt einen ausreichend guten Rechner ergeben, 
der für die ersten Schritte in diesem Umfeld ausreichend ist. 
Gemeint ist hier der <b>Raspberry Pi</b>, der in den letzten Jahren einen wahren Boom im Bereich "Internet-of-Things" ausgelöst hat. 
Dieser ist ein vollwertiger Linux-PC, der zwar nicht die Leistungsfähigkeit eines Spiele-PCs hat, für die alltäglichen Dinge, 
wie Lernspiele, Antolin-Webseite, Logo-Nachrichten usw. mehr als geeignet ist. 
Im Folgenden ist eine Beispielkonfiguration aufgelistet, die in Summe <b>134,- EUR</b> gekostet hat um ein Komplettsystem auf den Schreibtisch zu bringen.
</p>
<p>
Neben der Hardware ist eine einfache Oberfläche nötig, die die Bedienung durch das Kind gestaltet, so dass wenig Frust aufkommt, 
aber genug Potenzial bietet, sodass der vollständige Umgang mit einem Computer erlernt werden kann. 
</p>


